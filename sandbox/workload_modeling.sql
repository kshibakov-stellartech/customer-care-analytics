        AVG(CASE WHEN is_human_touched THEN CAST(frt_breach_10 AS DOUBLE) END) AS frt_breach_10_rate_human,
        AVG(CASE WHEN is_human_touched THEN CAST(frt_within_10 AS DOUBLE) END) AS frt_within_10_rate_human,
        AVG(CASE WHEN is_human_touched THEN CAST(frt_within_60 AS DOUBLE) END) AS frt_within_60_rate_human,

        AVG(CASE WHEN is_human_touched THEN first_reply_time END) AS frt_avg_min_human,
        approx_percentile(CASE WHEN is_human_touched THEN first_reply_time END, 0.50) AS frt_p50_min_human,
        approx_percentile(CASE WHEN is_human_touched THEN first_reply_time END, 0.80) AS frt_p80_min_human,
        approx_percentile(CASE WHEN is_human_touched THEN first_reply_time END, 0.90) AS frt_p90_min_human,

        -- routing proxy (human only)
        AVG(CASE WHEN is_human_touched THEN first_assign_time END) AS first_assign_avg_min_human,
        approx_percentile(CASE WHEN is_human_touched THEN first_assign_time END, 0.50) AS first_assign_p50_min_human,
        approx_percentile(CASE WHEN is_human_touched THEN first_assign_time END, 0.90) AS first_assign_p90_min_human,

        -- workload/complexity proxies (human only)
        AVG(CASE WHEN is_human_touched THEN handling_time_min END) AS handling_avg_min_human,
        approx_percentile(CASE WHEN is_human_touched THEN handling_time_min END, 0.50) AS handling_p50_min_human,
        approx_percentile(CASE WHEN is_human_touched THEN handling_time_min END, 0.90) AS handling_p90_min_human,

        AVG(CASE WHEN is_human_touched THEN reply_cnt END) AS reply_cnt_avg_human,
        approx_percentile(CASE WHEN is_human_touched THEN reply_cnt END, 0.90) AS reply_cnt_p90_human,

        AVG(CASE WHEN is_human_touched THEN agent_cnt END) AS agent_cnt_avg_human,
        approx_percentile(CASE WHEN is_human_touched THEN agent_cnt END, 0.90) AS agent_cnt_p90_human,

        AVG(CASE WHEN is_human_touched THEN assign_cnt END) AS assign_cnt_avg_human,
        AVG(CASE WHEN is_human_touched THEN uniq_assign_cnt END) AS uniq_assign_cnt_avg_human,

        AVG(CASE WHEN is_human_touched THEN reopen_count END) AS reopen_cnt_avg_human,

        -- keep extra signals if you want
        AVG(CAST(auto_involved AS DOUBLE)) AS auto_involved_share_all

    FROM ticket_fact
    GROUP BY 1,2,3
),
    agent_hour_activity AS (
    SELECT
        CAST(assign_created_at AS DATE) AS created_date,
        EXTRACT(HOUR from assign_created_at) AS created_hour,
        day_of_week(CAST(assign_created_at AS DATE)) AS weekday,
        msg_author_id AS agent_id,
        COUNT(msg_event_id) AS reply_events_cnt,
        COUNT(DISTINCT ticket_id) AS unique_tickets_replied_cnt,
        MIN(assign_created_at) AS first_reply_event_at,
        MAX(assign_created_at) AS last_reply_event_at,
        COUNT(msg_event_id) > 0 AS active_flag
    FROM agent_msg_num
    GROUP BY 1,2,3,4
),
    hourly_capacity AS (
    SELECT
        created_date,
        created_hour,
        weekday,

        COUNT(DISTINCT CASE
            WHEN active_flag AND agent_id <> 26440502459665 THEN agent_id
        END) AS active_agents,

        SUM(CASE WHEN agent_id <> 26440502459665 THEN reply_events_cnt ELSE 0 END) AS total_replies,
        SUM(CASE WHEN agent_id <> 26440502459665 THEN unique_tickets_replied_cnt ELSE 0 END) AS tickets_touched,

        CAST(SUM(CASE WHEN agent_id <> 26440502459665 THEN reply_events_cnt ELSE 0 END) AS DOUBLE)
        / NULLIF(COUNT(DISTINCT CASE WHEN active_flag AND agent_id <> 26440502459665 THEN agent_id END), 0)
        AS replies_per_active_agent,

        CAST(SUM(CASE WHEN agent_id <> 26440502459665 THEN unique_tickets_replied_cnt ELSE 0 END) AS DOUBLE)
        / NULLIF(COUNT(DISTINCT CASE WHEN active_flag AND agent_id <> 26440502459665 THEN agent_id END), 0)
        AS tickets_per_active_agent,

        MIN(CASE WHEN agent_id <> 26440502459665 THEN first_reply_event_at END) AS hour_first_reply_event_at,
        MAX(CASE WHEN agent_id <> 26440502459665 THEN last_reply_event_at  END) AS hour_last_reply_event_at

    FROM agent_hour_activity
    GROUP BY 1,2,3
),
    model_base_hourly AS (
    SELECT
        t.created_date,
        t.created_hour,
        t.weekday,

        -- volumes (all + human)
        t.tickets_total,
        t.tickets_auto,
        t.tickets_human,
        t.auto_resolved_share,
        t.human_share,

        -- SLA (human)
        t.frt_breach_10_rate_human,
        t.frt_avg_min_human,
        t.frt_p80_min_human,
        t.frt_p90_min_human,
        t.first_assign_p90_min_human,

        -- complexity proxies (human)
        t.handling_avg_min_human,
        t.handling_p90_min_human,
        t.reply_cnt_avg_human,
        t.agent_cnt_avg_human,

        -- capacity
        af.agents as scheduled_agents,
        COALESCE(c.active_agents, 0) AS active_agents,
        COALESCE(c.total_replies, 0) AS total_replies,
        COALESCE(c.tickets_touched, 0) AS tickets_touched,
        c.replies_per_active_agent,

        -- key features for fitting (human tickets per agent)
        CAST(t.tickets_human AS DOUBLE) / NULLIF(COALESCE(c.active_agents, 0), 0) AS human_tickets_per_active_agent,

        -- diagnostics
        CAST(COALESCE(c.total_replies, 0) AS DOUBLE) / NULLIF(t.tickets_human, 0) AS replies_per_human_ticket_observed,

        CAST(t.tickets_human * COALESCE(t.reply_cnt_avg_human, 1) AS DOUBLE)
            / NULLIF(COALESCE(c.total_replies, 0), 0) AS complexity_pressure_human

    FROM hourly_ticket_load t
    LEFT JOIN hourly_capacity c
        ON t.created_date = c.created_date
       AND t.created_hour = c.created_hour
    LEFT JOIN agent_fact af ON t.created_date = af.dt
                           AND t.created_hour = af.hour
)

SELECT *
FROM model_base_hourly
ORDER BY created_date, created_hour
;